# ── Gate 1: Device Identity Body Consistency ────────────────────────────────


def evaluate_device_domain_consistency_gate(
    cer_body_text: str,
    device_profile: dict[str, Any] | None,
) -> dict[str, Any]:
    """Gate 1: Scan CER body for forbidden terms from the device's domain.

    Args:
        cer_body_text: Full CER markdown body text.
        device_profile: device_profile.json content.

    Returns:
        Gate result dict with status, findings, and quarantine routing info.
    """
    if not cer_body_text.strip():
        return {
            "gate": "Gate 1 — Device Domain Consistency",
            "gate_id": "WRITER_GATE_01",
            "status": "HARD_FAIL",
            "reason": "CER body text is empty; final-body domain consistency cannot be verified.",
            "findings": [{
                "type": "empty_final_body",
                "severity": "HARD_FAIL",
                "message": "Writer produced no submission-facing CER body.",
            }],
            "warnings": [],
            "quarantine": True,
            "message": "HARD_FAIL: CER body text is empty.",
        }

    if not device_profile:
        return {
            "gate": "Gate 1 — Device Domain Consistency",
            "gate_id": "WRITER_GATE_01",
            "status": "SKIPPED",
            "reason": "No device_profile available; cannot resolve domain.",
            "findings": [],
            "warnings": [],
            "quarantine": False,
        }

    device_domain = device_profile.get("device_domain") or device_profile.get("clinical_domain") or ""
    domain_key = resolve_domain(device_domain)

    if not domain_key:
        return {
            "gate": "Gate 1 — Device Domain Consistency",
            "gate_id": "WRITER_GATE_01",
            "status": "BLOCKED",
            "reason": f"Unrecognised device_domain '{device_domain}'; cannot map to domain term matrix. Domain must be registered in DEVICE_DOMAIN_TO_MATRIX_DOMAIN before Writer can run.",
            "device_domain": device_domain,
            "findings": [{
                "finding_id": "DOMAIN-UNREGISTERED-001",
                "severity": "CRITICAL",
                "message": f"Device domain '{device_domain}' is not registered in the domain term matrix. Writer QA Gate 1 cannot validate domain consistency.",
                "remediation": f"Add '{device_domain}' to DEVICE_DOMAIN_TO_MATRIX_DOMAIN in domain_term_matrix.py.",
            }],
            "warnings": [],
            "quarantine": True,
        }

    domain_entry = DOMAIN_TERM_MATRIX.get(domain_key, {})
    forbidden_terms = get_forbidden_terms(domain_key)
    exception_contexts = get_exception_contexts(domain_key)
    ambiguous_terms = get_ambiguous_terms(domain_key)
    required_terms = get_required_terms(domain_key)

    # Split CER body: skip Annex sections for Gate 1 scan
    annex_marker = _find_annex_boundary(cer_body_text)
    scan_text = cer_body_text[:annex_marker] if annex_marker > 0 else cer_body_text

    findings: list[dict] = []
    warnings: list[dict] = []
    hard_fail = False

    # ── Forbidden terms scan ──
    scan_lower = scan_text.lower()
    for term in forbidden_terms:
        term_lower = term.lower()
        pos = 0
        while True:
            idx = scan_lower.find(term_lower, pos)
            if idx == -1:
                break
            surrounding = scan_text[max(0, idx - 200):idx + len(term) + 200]
            in_exception = _is_exception_context(surrounding, term_lower, exception_contexts)
            section = _guess_section(scan_text, idx)

            if not in_exception:
                findings.append({
                    "term": term,
                    "position": idx,
                    "section": section,
                    "surrounding_text": surrounding.strip()[:200],
                    "in_exception_context": False,
                })
                hard_fail = True
                break  # First HARD FAIL stops the scan per spec
            pos = idx + 1

        if hard_fail:
            break

    # ── Ambiguous terms scan (warnings only, never HARD FAIL) ──
    if not hard_fail:
        for term in ambiguous_terms:
            term_lower = term.lower()
            if term_lower in scan_lower:
                warnings.append({
                    "type": "ambiguous_term",
                    "term": term,
                    "message": f"Ambiguous term '{term}' found without sufficient required-term context.",
                })

    # ── Required terms coverage check (warnings only) ──
    if not hard_fail and required_terms:
        matched = sum(1 for t in required_terms if t.lower() in scan_lower)
        match_rate = matched / len(required_terms) if required_terms else 1.0
        if match_rate < 0.30:
            warnings.append({
                "type": "required_term_coverage",
                "match_rate": round(match_rate, 2),
                "matched": matched,
                "total": len(required_terms),
                "message": f"Required term match rate {match_rate:.0%} < 30%; possible domain mismatch.",
            })

    return {
        "gate": "Gate 1 — Device Domain Consistency",
        "gate_id": "WRITER_GATE_01",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "domain_key": domain_key,
        "device_domain": device_domain,
        "device_name": device_profile.get("device_name", "unknown"),
        "forbidden_terms_scanned": len(forbidden_terms),
        "findings": findings,
        "warnings": warnings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(findings)} forbidden term(s) found in non-exception context."
            if hard_fail
            else f"PASS: No forbidden terms in non-exception context. {len(warnings)} warning(s)."
        ),
    }


# ── Gate 3: Evidence-to-Conclusion Consistency ─────────────────────────────


def evaluate_evidence_conclusion_gate(
    cer_body_text: str,
    claim_support_matrix: dict[str, Any] | None,
) -> dict[str, Any]:
    """Gate 3: Verify Summary/Conclusion phrasing matches claim support levels.

    Checks that forbidden phrases for each claim's support_level do NOT appear
    in the Summary and Conclusions sections unless negated.
    """
    if not claim_support_matrix:
        return {
            "gate": "Gate 3 — Evidence-Conclusion Consistency",
            "gate_id": "WRITER_GATE_03",
            "status": "SKIPPED",
            "reason": "No claim_support_matrix available.",
            "findings": [],
            "quarantine": False,
        }

    # Determine the most restrictive support_level across all claims
    # If ANY claim is INSUFFICIENT, apply INSUFFICIENT policy to the whole conclusion.
    worst_level = _worst_support_level(claim_support_matrix)

    if worst_level is None:
        return {
            "gate": "Gate 3 — Evidence-Conclusion Consistency",
            "gate_id": "WRITER_GATE_03",
            "status": "SKIPPED",
            "reason": "No recognisable support_level found in claim_support_matrix.",
            "findings": [],
            "quarantine": False,
        }

    policy_level = resolve_policy_level(worst_level)
    if not policy_level:
        return {
            "gate": "Gate 3 — Evidence-Conclusion Consistency",
            "gate_id": "WRITER_GATE_03",
            "status": "SKIPPED",
            "reason": f"Cannot resolve policy level for '{worst_level}'.",
            "findings": [],
            "quarantine": False,
        }

    forbidden_phrases = get_forbidden_phrases(policy_level)
    if not forbidden_phrases:
        return {
            "gate": "Gate 3 — Evidence-Conclusion Consistency",
            "gate_id": "WRITER_GATE_03",
            "status": "PASS",
            "policy_level": policy_level,
            "worst_support_level": worst_level,
            "message": "STRONG level — no forbidden phrases; automatic PASS.",
            "findings": [],
            "quarantine": False,
        }

    # Extract Summary and Conclusions sections
    summary_text = _extract_section(cer_body_text, "SUMMARY") or ""
    conclusions_text = _extract_section(cer_body_text, "CONCLUSIONS") or ""
    # Also scan §4.x claim-specific conclusions
    device_eval_text = _extract_section(cer_body_text, "DEVICE UNDER EVALUATION") or ""

    combined = f"{summary_text}\n{conclusions_text}\n{device_eval_text}"

    all_findings = scan_for_forbidden_phrases(combined, forbidden_phrases)
    hard_fail_findings = [f for f in all_findings if f["triggers_fail"]]
    negated_findings = [f for f in all_findings if f["negated"]]

    hard_fail = len(hard_fail_findings) > 0

    return {
        "gate": "Gate 3 — Evidence-Conclusion Consistency",
        "gate_id": "WRITER_GATE_03",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "policy_level": policy_level,
        "worst_support_level": worst_level,
        "forbidden_phrases_scanned": len(forbidden_phrases),
        "total_matches": len(all_findings),
        "hard_fail_count": len(hard_fail_findings),
        "negated_pass_count": len(negated_findings),
        "findings": hard_fail_findings,
        "negated_findings": negated_findings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(hard_fail_findings)} forbidden phrase(s) found in conclusion text "
            f"without negation (policy level: {policy_level})."
            if hard_fail
            else f"PASS: No un-negated forbidden phrases in conclusion text "
                 f"(policy level: {policy_level}, {len(negated_findings)} negated match(es) allowed)."
        ),
    }


# ── Gate 2: IFU Fact Consumption Gate ──────────────────────────────────────


def evaluate_ifu_consumption_gate(
    cer_body_text: str,
    device_profile: dict[str, Any] | None,
) -> dict[str, Any]:
    """Gate 2: Check that IFU data was consumed, not placeholdered.

    Scans for 'Not extracted from IFU source text' patterns. If the device_profile
    shows IFU sources exist, these placeholders → GATE FAIL.
    """
    if not device_profile:
        return {
            "gate": "Gate 2 — IFU Fact Consumption",
            "gate_id": "WRITER_GATE_02",
            "status": "SKIPPED",
            "reason": "No device_profile available.",
            "findings": [],
            "quarantine": False,
        }

    profile_source_ids = device_profile.get("profile_source_ids") or device_profile.get("identity_supporting_source_types") or []
    has_ifu_source = bool(profile_source_ids)

    findings: list[dict] = []
    for pattern in IFU_PLACEHOLDER_PATTERNS:
        pos = 0
        while True:
            idx = cer_body_text.find(pattern, pos)
            if idx == -1:
                break
            findings.append({
                "pattern": pattern,
                "position": idx,
                "surrounding_text": cer_body_text[max(0, idx - 40):idx + len(pattern) + 40].strip(),
            })
            pos = idx + 1

    unique_fields = len({f.get("surrounding_text", "")[:40] for f in findings})
    hard_fail = has_ifu_source and len(findings) > 0

    return {
        "gate": "Gate 2 — IFU Fact Consumption",
        "gate_id": "WRITER_GATE_02",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "has_ifu_source": has_ifu_source,
        "placeholder_count": len(findings),
        "findings": findings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(findings)} IFU placeholder(s) found despite available IFU source data."
            if hard_fail
            else f"PASS: {'No IFU placeholders found' if not findings else 'IFU placeholders acceptable — no IFU source available'}."
        ),
    }


# ── Gate 4: Submission Body Cleanliness ─────────────────────────────────────


def evaluate_body_cleanliness_gate(
    cer_body_text: str,
) -> dict[str, Any]:
    """Gate 4: Scan CER body for banned internal system language strings.

    These strings must NEVER appear in the submission-facing CER body.
    Annex sections are excluded from the scan (structural headings like
    'Annex J MCP Execution...' are template boilerplate, not leakage).
    """
    if not cer_body_text.strip():
        return {
            "gate": "Gate 4 — Submission Body Cleanliness",
            "gate_id": "WRITER_GATE_04",
            "status": "HARD_FAIL",
            "banned_strings_scanned": len(BANNED_INTERNAL_STRINGS),
            "finding_count": 1,
            "findings": [{
                "type": "empty_final_body",
                "severity": "HARD_FAIL",
                "message": "Writer produced no submission-facing CER body.",
            }],
            "quarantine": True,
            "message": "HARD_FAIL: CER body text is empty.",
        }

    # Exclude Annex sections from scan
    annex_marker = _find_annex_boundary(cer_body_text)
    scan_text = cer_body_text[:annex_marker] if annex_marker > 0 else cer_body_text

    findings: list[dict] = []
    for banned in BANNED_INTERNAL_STRINGS:
        pos = 0
        banned_lower = banned.lower()
        text_lower = scan_text.lower()
        while True:
            idx = text_lower.find(banned_lower, pos)
            if idx == -1:
                break
            findings.append({
                "banned_string": banned,
                "position": idx,
                "surrounding_text": scan_text[max(0, idx - 40):idx + len(banned) + 40].strip(),
            })
            pos = idx + 1

    hard_fail = len(findings) > 0

    return {
        "gate": "Gate 4 — Submission Body Cleanliness",
        "gate_id": "WRITER_GATE_04",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "banned_strings_scanned": len(BANNED_INTERNAL_STRINGS),
        "finding_count": len(findings),
        "findings": findings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(findings)} banned internal string(s) found in CER body."
            if hard_fail
            else f"PASS: No banned internal strings in CER body."
        ),
    }


