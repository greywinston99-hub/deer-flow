def evaluate_render_boundary_gate(cer_body_text: str) -> dict[str, Any]:
    """Gate 5: Detect internal metadata leakage in the rendered CER body."""
    findings = []
    for pattern in RENDER_BOUNDARY_PATTERNS:
        if pattern in cer_body_text:
            line_idx = cer_body_text.index(pattern)
            context = cer_body_text[max(0, line_idx - 40):line_idx + len(pattern) + 60]
            findings.append({
                "pattern": pattern,
                "context": context.strip()[:200],
                "severity": "HARD_FAIL",
            })
    hard_fail = len(findings) > 0
    return {
        "gate": "Gate 5 — Render Boundary",
        "gate_id": "WRITER_GATE_05",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "patterns_scanned": len(RENDER_BOUNDARY_PATTERNS),
        "finding_count": len(findings),
        "findings": findings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(findings)} internal metadata pattern(s) leaked into CER body."
            if hard_fail
            else "PASS: No internal metadata in CER body."
        ),
    }


def build_context_contamination_trace(
    cer_body_text: str,
    device_profile: dict[str, Any] | None,
    state: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Build CONTEXT_CONTAMINATION_TRACE for every forbidden term found in CER."""
    state = state or {}
    trace_entries: list[dict[str, Any]] = []
    if not device_profile:
        return {"entries": [], "forbidden_term_count": 0}
    domain = device_profile.get("device_domain") or device_profile.get("clinical_domain") or ""
    domain_key = resolve_domain(domain)
    if not domain_key:
        return {"entries": [], "forbidden_term_count": 0}
    forbidden_terms = get_forbidden_terms(domain_key)
    for term in forbidden_terms:
        if term.lower() not in cer_body_text.lower():
            continue
        origin = "unknown/model_generation"
        source_texts = [
            str(state.get("device_profile", {}).get("intended_purpose", "")),
            str(state.get("device_profile", {}).get("mode_of_action", "")),
        ]
        for item in state.get("source_inventory") or []:
            source_texts.append(str(item.get("text") or ""))
        for item in state.get("search_run_registry") or []:
            source_texts.append(str(item.get("query") or ""))
        for row in state.get("sota_benchmark_matrix") or []:
            source_texts.append(str(row.get("endpoint") or ""))
        combined = " ".join(source_texts).lower()
        if term.lower() in combined:
            origin = "source_file_or_prior_artifact"
        trace_entries.append({
            "forbidden_term": term,
            "origin": origin,
            "domain": domain_key,
            "recommended_action": "Remove from CER body or add to exception_contexts",
        })
    return {
        "entries": trace_entries,
        "forbidden_term_count": len(trace_entries),
        "domain": domain_key,
    }


# ── Gate 6: Claim/BR-to-Text Consistency ─────────────────────────────────────


def evaluate_claim_br_text_consistency_gate(
    cer_body_text: str,
    claim_support_matrix: dict[str, Any] | None = None,
    benefit_risk_ledger: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    """Gate 6: Verify CER body does not contradict claim/BR ledger."""
    findings = []
    text_lower = cer_body_text.lower()

    # Check claim consistency
    if claim_support_matrix:
        for claim_id, row in claim_support_matrix.items():
            if not isinstance(row, dict) or not claim_id.startswith("C-"):
                continue
            support = str(row.get("support_level") or row.get("support_status") or "").upper()
            conclusion = str(row.get("max_conclusion_strength") or row.get("conclusion_strength") or "").upper()
            if support in {"INSUFFICIENT", "ALLOWED_USE_BLOCKED"} or conclusion in {"NOT_ALLOWED", "INSUFFICIENT"}:
                claim_pattern = claim_id.lower()
                if claim_pattern in text_lower:
                    nearby = _extract_nearby_text(cer_body_text, claim_id, 300)
                    overclaim_signals = [
                        "supported by", "demonstrates", "confirms", "proves",
                        "sufficient evidence", "robust evidence", "strong evidence",
                        "favourable benefit", "clinical benefit is", "device is safe and effective",
                    ]
                    for signal in overclaim_signals:
                        if signal in nearby.lower():
                            findings.append({
                                "claim_id": claim_id,
                                "support_level": support,
                                "contradiction": f"Claim marked {support} but text contains '{signal}'",
                                "context": nearby[:200],
                                "severity": "HARD_FAIL",
                            })

    # Check BR ledger consistency
    if benefit_risk_ledger:
        for row in benefit_risk_ledger:
            balance = str(row.get("benefit_risk_balance") or "").lower()
            guard = str(row.get("conclusion_guard_result") or "").lower()
            if "unfavourable" in balance or "not supported" in guard or "unfavorable" in balance:
                if any(signal in text_lower for signal in [
                    "favourable benefit-risk", "favorable benefit-risk",
                    "positive benefit-risk", "overall clinical benefit is confirmed",
                    "device is considered safe and effective",
                ]):
                    findings.append({
                        "balance": balance,
                        "guard": guard,
                        "contradiction": "BR ledger shows unfavourable/not-supported but text implies favourable",
                        "severity": "HARD_FAIL",
                    })
                    break

    hard_fail = len(findings) > 0
    return {
        "gate": "Gate 6 — Claim/BR Text Consistency",
        "gate_id": "WRITER_GATE_06",
        "status": "HARD_FAIL" if hard_fail else "PASS",
        "finding_count": len(findings),
        "findings": findings,
        "quarantine": hard_fail,
        "message": (
            f"HARD_FAIL: {len(findings)} claim/BR-to-text contradiction(s) found."
            if hard_fail
            else "PASS: CER body consistent with claim/BR ledger."
        ),
    }


def _extract_nearby_text(text: str, target: str, window: int) -> str:
    idx = text.lower().find(target.lower())
    if idx < 0:
        return ""
    start = max(0, idx - window // 2)
    end = min(len(text), idx + len(target) + window // 2)
    return text[start:end]


def run_all_writer_gates(
    cer_body_text: str,
    device_profile: dict[str, Any] | None = None,
    claim_support_matrix: dict[str, Any] | None = None,
    benefit_risk_ledger: list[dict[str, Any]] | None = None,
    state: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Run all writer remediation gates (1-6) against CER body text.

    Returns a combined result with per-gate status and an overall pass/fail.
    """
    gate1 = evaluate_device_domain_consistency_gate(cer_body_text, device_profile)
    gate2 = evaluate_ifu_consumption_gate(cer_body_text, device_profile)
    gate3 = evaluate_evidence_conclusion_gate(cer_body_text, claim_support_matrix)
    gate4 = evaluate_body_cleanliness_gate(cer_body_text)
    gate5 = evaluate_render_boundary_gate(cer_body_text)
    gate6 = evaluate_claim_br_text_consistency_gate(cer_body_text, claim_support_matrix, benefit_risk_ledger)

    gates = [gate1, gate2, gate3, gate4, gate5, gate6]
    any_hard_fail = any(g.get("status") == "HARD_FAIL" for g in gates)
    any_blocked = any(g.get("status") == "BLOCKED" for g in gates)
    quarantined = any(g.get("quarantine") for g in gates)

    # S6 fix: also check writer invocation and blocker status
    state = state or {}
    blocker = state.get("blocker_report") or {}
    writer_invoked = blocker.get("writer_invoked", True)

    contamination_trace = build_context_contamination_trace(cer_body_text, device_profile, state) if device_profile else {}

    if any_hard_fail:
        overall = "HARD_FAIL"
    elif any_blocked or not writer_invoked:
        overall = "BLOCKED_WITH_COMPROMISE"
    elif quarantined:
        overall = "PASS_WITH_QUARANTINE"
    else:
        overall = "PASS"
    return {
        "overall_status": overall,
        "quarantine": quarantined,
        "gates": {
            "gate_1_domain_consistency": gate1,
            "gate_2_ifu_consumption": gate2,
            "gate_3_evidence_conclusion": gate3,
            "gate_4_body_cleanliness": gate4,
            "gate_5_render_boundary": gate5,
            "gate_6_claim_br_consistency": gate6,
        },
        "context_contamination_trace": contamination_trace,
        "message": (
            "One or more writer gates HARD_FAIL; report must be quarantined."
            if quarantined
            else "All writer gates PASS; report may proceed to release candidate."
        ),
    }


# ── W3: QA Gate Hardening (Gate 5) ─────────────────────────────────────────


def evaluate_remediated_qa_gate(
    cer_body_text: str,
    device_profile: dict[str, Any] | None = None,
    claim_support_matrix: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Gate 5: Remediated QA gate — replaces Annex J / benchmark-style QA.

    Runs domain consistency, forbidden terms, evidence-conclusion consistency,
    internal language leakage, and IFU placeholder checks. Produces per-item
    findings. Never outputs score 100 / findings empty on contaminated reports.

    Returns a structured QA report: {status, score, dimensions, findings, message}
    """
    gate1 = evaluate_device_domain_consistency_gate(cer_body_text, device_profile)
    gate2 = evaluate_ifu_consumption_gate(cer_body_text, device_profile)
    gate3 = evaluate_evidence_conclusion_gate(cer_body_text, claim_support_matrix)
    gate4 = evaluate_body_cleanliness_gate(cer_body_text)

    dimensions = {
        "domain_consistency": {
            "label": "Device Domain Body Consistency",
            "status": _qa_dimension_status(gate1),
            "findings": gate1.get("findings", []),
            "warnings": gate1.get("warnings", []),
        },
        "evidence_conclusion_consistency": {
            "label": "Evidence-to-Conclusion Consistency",
            "status": _qa_dimension_status(gate3),
            "findings": gate3.get("findings", []),
            "negated_pass": gate3.get("negated_pass_count", 0),
        },
        "ifu_consumption": {
            "label": "IFU Fact Consumption",
            "status": _qa_dimension_status(gate2),
            "placeholder_count": gate2.get("placeholder_count", 0),
            "has_ifu_source": gate2.get("has_ifu_source", False),
        },
        "body_cleanliness": {
            "label": "Submission Body Cleanliness",
            "status": _qa_dimension_status(gate4),
            "findings": gate4.get("findings", []),
        },
    }

    all_findings: list[dict] = []
    for dim_key, dim in dimensions.items():
        for f in dim.get("findings", []):
            f_with_dim = dict(f)
            f_with_dim["dimension"] = dim_key
            all_findings.append(f_with_dim)

    hard_fail_dims = [k for k, d in dimensions.items() if d["status"] == "FAIL"]
    warning_dims = [k for k, d in dimensions.items() if d["status"] == "WARNING"]
    pass_dims = [k for k, d in dimensions.items() if d["status"] == "PASS"]
    skipped_dims = [k for k, d in dimensions.items() if d["status"] == "SKIPPED"]

    if hard_fail_dims:
        overall_status = "FAIL"
        score = max(0, 100 - len(hard_fail_dims) * 25)
    elif warning_dims:
        overall_status = "PASS"
        score = max(70, 100 - len(warning_dims) * 10)
    elif skipped_dims:
        overall_status = "PASS"
        score = 80
    else:
        overall_status = "PASS"
        score = 100

    return {
        "gate": "Gate 5 — Remediated QA (Content + Domain + Evidence + Cleanliness)",
        "gate_id": "WRITER_GATE_05",
        "status": overall_status,
        "score": score,
        "dimensions": dimensions,
        "failing_dimensions": hard_fail_dims,
        "warning_dimensions": warning_dims,
        "total_findings": len(all_findings),
        "hard_fail_findings": len([f for f in all_findings if f.get("triggers_fail", True)]),
        "findings": all_findings[:50],  # Cap at 50 for report size
        "quarantine": len(hard_fail_dims) > 0,
        "message": _qa_message(overall_status, hard_fail_dims, warning_dims, score),
    }


def _qa_dimension_status(gate_result: dict[str, Any]) -> str:
    """Map a gate result to a QA dimension status."""
    status = gate_result.get("status", "SKIPPED")
    if status == "HARD_FAIL":
        return "FAIL"
    if status == "PASS":
        if gate_result.get("warnings"):
            return "WARNING"
        return "PASS"
    return "SKIPPED"


def _qa_message(
    overall_status: str,
    failing_dims: list[str],
    warning_dims: list[str],
    score: int,
) -> str:
    """Build a human-readable QA message."""
    if overall_status == "FAIL":
        return (
            f"QA FAIL (score {score}): "
            f"{len(failing_dims)} dimension(s) failed — {', '.join(failing_dims)}. "
            f"Report is NOT ready for release."
        )
    if overall_status == "PASS_WITH_WARNINGS":
        return (
            f"QA PASS with warnings (score {score}): "
            f"{len(warning_dims)} dimension(s) have warnings — {', '.join(warning_dims)}."
        )
    if overall_status == "PASS_WITH_SKIPS":
        return f"QA PASS with skips (score {score}): some checks could not run."
    return f"QA PASS (score {score}): All content and domain checks passed."


