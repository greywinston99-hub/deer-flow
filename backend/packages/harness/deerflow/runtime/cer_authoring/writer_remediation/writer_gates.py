def _find_annex_boundary(text: str) -> int:
    """Find where Annex sections start in the CER body.

    Returns the character position of the first Annex heading, or -1 if not found.
    """
    annex_patterns = [
        r"\n#+\s*Annex\s",
        r"\nAnnex\s+[A-Z]",
        r"\n#+\s*附录\s",
    ]
    for pat in annex_patterns:
        m = re.search(pat, text, re.IGNORECASE)
        if m:
            return m.start()
    return -1


def _is_exception_context(surrounding: str, term: str, exception_contexts: list[str]) -> bool:
    """Check if a forbidden term appears in an exclusion/contrast context."""
    surrounding_lower = surrounding.lower()
    # Search exclusion clauses (NOT (term1 OR term2 OR ...))
    if re.search(r"\bnot\s*\([^)]*" + re.escape(term) + r"[^)]*\)", surrounding_lower):
        return True
    # Also check for broader search-query context: if the term appears in a line
    # that contains search-result or search-strategy markers
    if any(marker in surrounding_lower for marker in (
        "| count ", "| returned ", "search-sota", "search-due",
        "not (", "not(", "search strategy", "search-strategy",
        "| pubmed |", "| clinicaltrials", "| embase", "| cochrane",
        "search_run_registry", "search protocol",
    )):
        return True
    for ec in exception_contexts:
        if ec.lower() in surrounding_lower:
            ec_idx = surrounding_lower.find(ec.lower())
            term_idx = surrounding_lower.find(term.lower())
            if abs(ec_idx - term_idx) <= 80:
                return True
    return False


def _guess_section(text: str, pos: int) -> str:
    """Guess which CER section a position falls in."""
    # Look backward for the nearest markdown heading
    before = text[:pos]
    heading_matches = list(re.finditer(r"^#{1,4}\s+(.+)$", before, re.MULTILINE))
    if heading_matches:
        return heading_matches[-1].group(1).strip()[:80]
    return "(preamble)"


def _extract_section(text: str, section_label: str) -> str | None:
    """Extract a named section from CER markdown body.

    Finds a markdown heading (line starting with #) that contains the section label,
    then returns all text from that heading to the next heading of equal or higher level.
    """
    label_lower = section_label.lower()
    lines = text.split("\n")
    start_idx = None
    heading_level = 0

    # Find the heading line
    for i, line in enumerate(lines):
        stripped = line.strip()
        if stripped.startswith("#") and label_lower in stripped.lower():
            # Count heading level
            heading_level = len(stripped) - len(stripped.lstrip("#"))
            start_idx = i
            break

    if start_idx is None:
        return None

    # Extract from heading to next heading of same or higher level
    end_idx = len(lines)
    for i in range(start_idx + 1, len(lines)):
        stripped = lines[i].strip()
        if stripped.startswith("#"):
            level = len(stripped) - len(stripped.lstrip("#"))
            if level <= heading_level:
                end_idx = i
                break

    extracted = "\n".join(lines[start_idx:end_idx])
    return extracted[:5000]


def _worst_support_level(matrix: dict[str, Any]) -> str | None:
    """Extract the most restrictive support_level from a claim_support_matrix.

    Priority: INSUFFICIENT > CAUTIOUS > MODERATE > STRONG
    """
    LEVEL_RANK = {"INSUFFICIENT": 0, "CAUTIOUS": 1, "MODERATE": 2, "STRONG": 3}

    worst: str | None = None
    worst_rank = 99

    # Skip metadata keys
    SKIP_KEYS = {"retrieval_completeness", "schema_name", "provisional_mode"}

    for key, claim in matrix.items():
        if key in SKIP_KEYS or not isinstance(claim, dict):
            continue
        sl = claim.get("support_level") or claim.get("max_conclusion_strength")
        if sl:
            level = resolve_policy_level(str(sl))
            if level:
                rank = LEVEL_RANK.get(level, 99)
                if rank < worst_rank:
                    worst_rank = rank
                    worst = str(sl)

    return worst
